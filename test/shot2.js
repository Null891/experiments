const http=require("http"),fs=require("fs"),path=require("path"),puppeteer=require("puppeteer-core");
const ROOT=path.resolve("."),PORT=8078;
const MIME={".html":"text/html",".js":"text/javascript",".css":"text/css",".svg":"image/svg+xml"};
const CHROME=["C:/Program Files/Google/Chrome/Application/chrome.exe","C:/Program Files (x86)/Microsoft/Edge/Application/msedge.exe"].find(p=>fs.existsSync(p));
const srv=http.createServer((q,r)=>{let p=decodeURIComponent(q.url.split("?")[0]);if(p==="/")p="/index.html";const fp=path.join(ROOT,p);if(!fs.existsSync(fp)||fs.statSync(fp).isDirectory()){r.writeHead(404);return r.end();}r.writeHead(200,{"Content-Type":MIME[path.extname(fp)]||"application/octet-stream"});fs.createReadStream(fp).pipe(r);});
(async()=>{await new Promise(r=>srv.listen(PORT,r));
const b=await puppeteer.launch({headless:"new",executablePath:CHROME,args:["--no-sandbox","--use-gl=swiftshader","--enable-webgl","--ignore-gpu-blocklist"]});
const pg=await b.newPage();await pg.setViewport({width:1280,height:800});
await pg.goto(`http://localhost:${PORT}/index.html`,{waitUntil:"load",timeout:15000});
await new Promise(r=>setTimeout(r,1500));
await pg.screenshot({path:"/tmp/ui_splash.png"});console.log("splash");
await pg.evaluate(()=>{document.getElementById('splash').style.display='none';document.body.classList.add('locked');});
await new Promise(r=>setTimeout(r,2200));
await pg.screenshot({path:"/tmp/ui_hub.png"});console.log("hub");
await pg.evaluate(()=>document.getElementById('dirBtn').click());
await new Promise(r=>setTimeout(r,600));
await pg.evaluate(()=>{const s=document.getElementById('dirSearch');s.value='maze';s.dispatchEvent(new Event('input'));});
await new Promise(r=>setTimeout(r,300));
await pg.evaluate(()=>{const s=document.getElementById('dirSearch');s.value='';s.dispatchEvent(new Event('input'));});
await new Promise(r=>setTimeout(r,300));
await pg.screenshot({path:"/tmp/ui_dir.png"});console.log("directory");
await b.close();srv.close();})().catch(e=>{console.error(e.message);process.exit(1);});
